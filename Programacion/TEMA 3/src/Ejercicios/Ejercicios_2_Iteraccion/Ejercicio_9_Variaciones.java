package Ejercicios.Ejercicios_2_Iteraccion;

public class Ejercicio_9_Variaciones {

    public static void main(String[] args) {

        //Primer bucle que va desde 1111 hasta 1114
        for (int i = 1111; i <= 1114; i++) {
            System.out.println(i);
        }
        System.out.println("-------------------------");
        //Segundo bucle que va desde 2221 hasta 2224
        for (int j = 2221; j <= 2224; j++) {
            System.out.println(j);
        }
        System.out.println("-------------------------");

        //Tercer bucle que va desde 3331 hasta 3334
        for (int k = 3331; k <= 3334; k++) {
            System.out.println(k);
        }
        System.out.println("-------------------------");

        //Cuarto bucle que va desde 4441 hasta 4444
        for (int l = 4441; l <= 4444; l++) {
            System.out.println(l);

        }

    }

}
