package Ejemplos;

public class Operadores_2 {

    public static void main(String[] args) {

        int puntos =0;
        puntos=puntos+1;
        puntos++;
        System.out.println(puntos);

        //Incrementar de 2 en 2

        puntos=puntos+2;
        puntos+=2;
        System.out.println(puntos);

        //Decrementamos puntos en 1 unidad

        puntos=puntos-1;
        puntos--;
        System.out.println(puntos);

        
    }
}
